#!/usr/bin/env python3
"""
This module provides a simple function to add two floating point numbers.

It demonstrates the use of type annotations and includes proper documentation
for both the module and the function, as required by the project guidelines.
"""


def add(a: float, b: float) -> float:
    """
    Return the sum of two floating point numbers.

    Args:
        a (float): The first number to add.
        b (float): The second number to add.

    Returns:
        float: The sum of the two input numbers.
    """
    return a + b
